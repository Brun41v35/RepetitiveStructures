// Neste documento vamos utilizar a estrutura de repiticao for

for contador in 1...4 {
    print("Hello World")
}

for dia in 1...4 {
    print("Hoje é dia \(dia) de fevereiro")
}

let listaNome = ["Bruno","Joao", "Sergio", "Maria"]
for nome in listaNome {
    print("Nome: \(nome)")
}

// Estrutura de repeticao WHILE
var lifes = 10
while lifes > 0 {
        print("O jogador está com \(lifes) restante")
        lifes = lifes - 1
}
